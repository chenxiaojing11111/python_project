# and逻辑与：两个数据必须都为True，才返回True
r = True and True
print(r)
r = True and False
print(r)

# 逻辑或：只要两个有一个为True，结果就为True
r = True or False
print(r)
r = False or False
print(r)

# not逻辑非：取反操作
r = not True
print(r)
r = not False
print(r)
